    <h3 class="h_ttl_1">利用規約</h3>
    <div class="box_inner pt_15 pb_7 cf">
        <p>【制定日】<br>
            2014年4月15日 制定</p>
        <p class="mt_10">ジョイスペ事務局（以下「弊社」という）は、 joyspe （以下「本サービス」という）をご利用になる方（以下「利用者」という）のプライバシーを尊重し、利用者の大切な個人情報の保護に万全を尽くします</p>
        <dl>
            <dt><strong>第1条（目的）</strong></dt>
            <dd>
                <p><span>１.</span>「joyspe」会員利用規約（以下、本規約という）はジョイスペ事務局（以下、甲という）が運営する「joyspe」（以下当サイト）において応募者（以下、乙という）が、求職活動を行う際にトラブルや問題を事前に防止のための合意と理解を求めることを目的とする。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第2条（入会）</strong></dt>
            <dd>
                <p><span>１.</span> 当サイト入会希望者は本規約を承諾の上、当サイトの会員登録手続きに従い入会するものとします。</p>
                    <p><span>２.</span> 当サイトは、会員登録の申し込みについて、本システム内の申込フォームに記入することにより、ユーザーID及びパスワードを発行し会員とします。</p>
                    <p><span>３.</span> 当サイトは、必要に応じ本規約を変更することができるものとします。</p>
                    <p><span>４.</span> 当サイトは、高校生、18歳未満の方（高校生年齢の18歳含む）の利用を禁止します。</p>
                    <p><span>５.</span> 本規約は本登録後（身分確認後）より１ヶ月間とし以後は甲乙いずれかからの契約解除の申し出がない場合、自動的に更新するものとします。</p>
                    <p><span>６.</span>乙は甲に対し偽りなく生年月日・年齢・その他、登録に関する必要事項を申告しなければなりません。万一偽りがあった場合によって生じる条例及び法律的責任は全て乙に帰するものとし、甲は責任がないものとします。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第3条（変更事項）</strong></dt>
            <dd>
                <p><span>１</span>. 乙は、登録内容に変更が生じた場合は、当サイトの変更手続きに従い、当サイトに届けるものとする。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第4条（資格消失）</strong></dt>
            <dd>
                <p><span>１.</span> 本規約に反する行為があった場合。</p>
                    <p><span>２.</span> 当サイトの会員登録内容に、虚偽等の不正があった場合。</p>
                    <p><span>３.</span> 当サイトが会員として不適切と判断した場合。</p>
                    <p><span>４.</span> 18歳未満（高校生年齢）の場合。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第5条（禁止事項）</strong></dt>
            <dd>
                <p><span>１.</span> 当サイトの運営を妨げる行為、著者権等の利権を侵害する行為。</p>
                    <p><span>２.</span> 当サイトのプライバシーの侵害や、不利益などの損害を与える行為。</p>
                    <p><span>３.</span> 犯罪的行為もしくは犯罪的行為に結びつく行為、またそのおそれのある行為。</p>
                    <p><span>４.</span> アダルト的なものにあたる画像をアップロードする行為。</p>
                    <p><span>５.</span> ID及びパスワードを不正に使用する行為。</p>
                    <p><span>６.</span> 公序良俗に反する行為やその恐れのある一切の行為。</p>
                    <p><span>７.</span> 個人や団体など誹謗中傷する行為。</p>
                    <p><span>８.</span> 意図的に虚偽の情報を登録または提供する行為。</p>
                    <p><span>９.</span> 当サイトで得た情報を利用し営利を目的とした情報提供活動を行う行為。</p>
                    <p><span>10.</span> 当サイトが不適切と判断する行為。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第6条（ユーザーID及びパスワードの管理と責任）</strong></dt>
            <dd>
                <p><span>１.</span> 乙は自己のユーザーID及びパスワードを第三者に貸与または譲渡し、使用させることはできません。</p>
                    <p><span>２.</span> 乙は自己のユーザーID及びパスワードの管理、使用について一切の責任を負い、その使用に関する債務を支払うものとする。</p>
                    <p><span>３.</span> 乙は自己のユーザーID及びパスワードの盗難や、第三者に使用されている事が判明した場合、直ちに当サイトに報告し、当サイトからの指示があればこれに従うものとする。</p>
                    <p><span>４.</span> 乙は自らの責任に基づき当サービスを利用するものとし当サービスの利用に関する一切の責任を負うものとします。</p>
                    <p><span>５.</span> 乙が自ら提供した情報はその内容について一切の責任を負うものとします。</p>
                    <p><span>６.</span> 乙が当サイトに対して提供した情報内容に起因して求人掲載企業、その他の第三者との間で紛争等が生じた場合には乙自らの責任において解決し甲は何らの責任を負わず介入しない事とします。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第7条（サイト運用の保守、変更等）</strong></dt>
            <dd>
                <p><span>１.</span> 乙は当サイトの保守、点検などのため、または緊急的なシステムメンテナンス、サーバー故障、災害時により、予告なくサービスの一時停止に利用できなくなる場合があってもこれを承諾するものとする。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第8条（免責条項）</strong></dt>
            <dd>
                <p><span>１.</span>当サイトは会員がアップロードしたデータ等を甲所定のフォーマットに交換することがあり、そのためデータ等に品質劣化が生じたとしても、その責任をおわないものとする。</p>
                    <p><span>２.</span>甲は、会員が本サービス用設備に蓄積したデータ等の消失、他社による改ざんに関し、いかなる責任もおわないものとする。</p>
                    <p><span>３.</span>甲は、当サイトURLが会員の意図しない第三者に渡り、データ等が会員の意図しない第三者に利用される事態が生じた場合でも、その責を追わないものとする。</p>
                    <p><span>４.</span>甲は、本サービスの利用により発生した会員の損害（第三者との間で生じたトラブルに起因する損害を含む）及び当サービスを利用できなかったことにより発生した会員または他社の損害に関し、いかなる責任も負わないものとする。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第9条（プライバシー）</strong></dt>
            <dd>
                <p><span>１.</span>当サイトが収集し管理する会員の個人情報は原則として開示しません。ただし、法律等によって要求された場合や甲の利権・財産を保護する目的などにおいては開示する場合があります。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第10条（お祝い金の支払）</strong></dt>
            <dd>
                <p><span>１.</span>振込手数料については１回につき、事務手数料を含む一律540円を乙が負担するものとします。尚、精算時に口座情報に不備、誤りがあった場合は　事務手数料1,744円を差 し引いた金額を再度、振込するものとします。</p>
                    <p><span>２.</span>登録時に本人確認とし身分証明書の提出があります。身分証明書と認められるものは、顔写真つきもの（免許証、パスポート、住民基本台帳カード（住基ネット）、学生証、社員証）身分証明書の確認ができなかった場合、支払は行えないものとします。</p>
                    <p><span>３.</span>お祝い金は、応募した求人掲載企業が指定した条件を達成したのちに当サイトの「お祝い金申請」を行った時点でお祝い金の権利を得るものとします。</p>
                    <p><span>４.</span>お祝い金は、応募した求人掲載企業からの採用金の支払が発生した後に支払うものとします。万が一、採用金の支払が行われない場合、甲だけでは対応できない場合には、乙は甲が行う対応に協力するものとします。</p>
                    <p><span>５.</span>お祝い金は、スカウトメール受信時、応募時のお祝い金が支払対象となります。応募求人掲載企業がスカウトメール受信時、応募時からお祝い金を変更したとしても、スカウトメール受信時、応募時のお祝い金が支払対象となります。</p>
                    <p><span>６.</span>甲が、応募求人掲載企業から採用金を徴収不能になった場合、又は徴収不能と判断した場合は、甲が乙への支払を応募求人掲載企業からの徴収が完了するまで停止する事ができるものとします。</p>
                    <p><span>７.</span>乙が本規約に違反した場合のお祝い金は、乙に対し一切支払わない。又、本規約違反に抵触している恐れがある場合も審議完了までお祝い金の支払い、会員の権利を一時保留とします。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第11条（サービスの変更）</strong></dt>
            <dd>
                <p>甲は当サイトの運営を良好に保つ為に乙への事前の通知なくサービスを変更または一時的な中断をする事があり乙はそれに対して異議を申し立てないものとします。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第12条（利用規約の変更）</strong></dt>
            <dd>
                <p>甲は本規約を随時変更する事が出来るものとします。変更の内容については当ページへの変更をもって公表とします。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第13条（著作権）</strong></dt>
            <dd>
                <p><span>１.</span>当サイトに含まれるトレードマーク、キャラクター、文章、図表、画像、写真、営業秘密、プログラム、コンテンツ等の商標・特許・著作権・ノウハウ及び他の如何なる知的財産権に基づく権利（以下、「知的財産権等」）は、甲または甲にその利用を許諾した権利者に帰属しています。</p>
                    <p><span>２.</span>当サイトは乙が提供した著作物、その他の情報（文字、画像、音声、及びこれらの結合物を含む）全てを自由に変更、複製し当サイトの編集等に二次利用出来るものとします。乙が提供した著作物に関する著作権は当サイトの所有保有するものとします。</p>
                    <p><span>３.</span>第5条第1項に掲げる行為を行った場合、法的に罰せられるほか損害賠償を請求する事があります。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第14条（賠償）</strong></dt>
            <dd>
                <p><span>１.</span>乙は本規約の各号に違反した事により、その他乙の行為により甲が被った被害を賠償する責任がある事とします。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第15条（雇用）</strong></dt>
            <dd>
                <p><span>１.</span>甲と乙は雇用関係でない事を確認する。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第16条（譲渡）</strong></dt>
            <dd>
                <p><span>１.</span>本規約にもとづく、地位及び権利は譲渡できないものとします。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第17条（紛争）</strong></dt>
            <dd>
                <p><span>１.</span>本規約に関し、甲乙間に紛争が生じた場合は東京地方裁判所を管轄裁判所とすることを甲、乙は同意するものとします。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第18条（協議）</strong></dt>
            <dd>
                <p><span>１.</span>本規約に定めない事項については、必要に応じて甲乙協議の上定めることとします。</p>
            </dd>
        </dl>
        <dl>
            <dt><strong>第19条（裁判所）</strong></dt>
            <dd>
                <p><span>１.</span>本利用規約の準拠法は、日本法とします。 甲と乙の間で訴訟の必要が生じた場合、東京地方裁判所を第一審の専属的合意管轄裁判所とします。</p>
            </dd>
        </dl>
        <p class="btop_CCC pt_18"><strong>附則</strong></p>
        <p>本規約は2014年4月15日から施行します。本規約の施行前に会員によって行われた行為についても本規約が適用されます。以上、上記に上げる内容について全て合意いたします。</p>
