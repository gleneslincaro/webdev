							<p class="kaihu">狙ったエリアの会員へ自動でスカウトメールを送信することができます。<br />
								最大５つの条件へ送信が可能となっています。<br />
								エリアと状態（例：開封済み）から選択が可能でそれに合わせたテンプレートを選ぶことができます。<br />
								<br />
								※送信は毎日１回（早朝）にシステムが起動されます。 </p>
							<div class="contents-box1">
								<h2>はじめに：自動送信をクリック</h2>
								<p> 管理画面ヘッダー一番右に設置された「スカウト自動送信」をクリック</p>
