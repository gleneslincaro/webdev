				<dl>
					<dt>8.所属を明らかにする </dt>
					<dd><p>ジョイスペのスカウトメールではフッター箇所に店舗様のご登録内容を表示しておりますが、女の子の視点に立ってみると業界自体に不安を持っている子も少なくありません。<br />
						</p>
						<p>なるべく不安を解消するためにもお店の名前とは別にご担当者の名前を記載しましょう。<br /><br /></p>
						<p>店舗の情報でも同様になるべく情報は細かく記載することをお勧めします。<br />
						女の子が最もアクセスする場所としてはオフィシャルホームページです。</p>
						<p>中には担当の方が動画などに出演されているケースはより安心感を演出するうえでは絶大な素材と思われます。<br />
						動画の作成は予算が必要ですのでそんな場合はご担当者の写真だけでも添えているとイメージが変わります。<br />
						</p>
					</dd>
				</dl>
