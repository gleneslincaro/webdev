				<dl>
					<dt>9.好条件を出す時は安心感を演出 </dt>
					<dd><p>女の子に対して好条件を出して獲得をしたいのはやまやまですがここには気をつける点が一つあります。<br />
						やはり好条件を出すにはそれなりの安心感を演出することが大事です。<br /><br /></p>
						<p>とても安易な公式ですが以下の内容のような現象はすくなくありません。</p>
						<p>お祝い金が多い+女の子の在籍数が多い=みんなもらっているから安心お祝い金が多い+女の子が少ない=このお店で働くのは不安</p>
						<p>スタートアップのお店様には厳しい条件ではありますがこういう場合は少しでも安心してもらえるように8でも説明した通り、ご担当者の顔などを出して安心感を演出するのもひとつです。</p>
						<p>ジョイスペの会員の動向を分析していると入店したもしくは検討している会員の多くがオフィシャルホームページや匿名掲示板などで情報を収集していることがわかりました。<br /><br /></p>
						<p>つまりネットで自分なりに情報を集めてお店の雰囲気などを調査しています。<br />
						ここであまりネット上に情報のない店舗様があまりにも好条件を出してしまうとかえってあやしいという疑心を生み出してしまいます。<br /><br /></p>
						<p>最低限としてオフィシャルホームページを持つ、ネットのネガティブ情報に対策をする。<br />
						働く女の子に日記をつける。<br />
						前項でも書いた担当者の顔がわかるようにする。</p>
						<p>とにかく大事なことはお店の情報をなるべく多く持つことです。</p>
