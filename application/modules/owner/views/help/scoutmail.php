					<dt>5.大きな構成を意識する</dt>
					<dd><p>文字の量としては縦は3～4スクロール程度で収まる分量が理想です。</p>
						<p>スマホではユーザーのファーストビューはPCと比べ範囲が狭いです。<br />
						訴求力のある内容を冒頭に設置しましょう。</p>
						<p>とある検証では約57%の人が冒頭の内容で先を読むか読まないかを判断するという結果がでています。<br />
						この冒頭に他社との明らかな違いを記載することや引き付けるような文章を入れることを意識するといいでしょう。
						</p>
						<p>ジョイスペのスカウトメールは中身の部分にあたる箇所に店名や給与などの条件が記載されています。<br />
						よってより冒頭に力を入れる必要があります。</p>
