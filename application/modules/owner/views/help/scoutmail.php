					<dt>4.メッセージは<span>500文字以内</span></dt>
					<dd><p>文字の量としては縦は3～4スクロール程度で収まる分量が理想です。</p>
						<p>スマホの横の文字数「15文字程度」というのは、「現代人がひと目で意味を理解できる文字量」でもあります。<br />
						スマホに限らず、見出しやコピーなど、強いメッセージを込めたテキストは14文字以内を心がけてみるとよいと思います。</p>
						<p>2～3行程度の文節ならば、ざっと読み流しても、頭の中でスピーディに意味が理解できる文章に変換されます。<br />
						それを超えてしまうと、「読むの面倒だなぁ」と脳が拒絶してしまうのです。<br /><br /></p>
						<p>一文、一文も、なるべく短いほうが、読み手はすんなり頭に入ります。<br />
						「…ですけれど」「…かもしれませんが」といったフレーズをはさみながら、延々と続いていく文章。</p>
						<p>結論が見えず、読み手はうんざりしてしまいます。<br />
						3行目に突入して「。」がひとつもない文章でしたら、かならずとこかに切れるポイントがあります。</p>
						<p>「長文は悪文」と心得て、文章そのものにも見直しをかけてみてくださいね。加えて、適宜改行を入れるだけで、メルマガのビジュアルとしてはかなりスッキリしますよ。</p>
					</dd>
