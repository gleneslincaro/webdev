				<dl>
					<dt>7.コンテンツはひとつでも少ないほうがいい</dt>
					<dd><p>スカウトメールの最終目的は、クリックして自社のページに飛んでもらうこと。<br />
						このことを大前提として、いつも頭の中に置いてください。</p>
						<p>スカウトメールは、たくさんの情報を伝える場ではありません。<br />
						ところがそのことを忘れ、「ひとつでもたくさんのことを伝えたい！」となってしまった場合、<br />１通のスカウトメールに複数のコンテンツがギューギュー詰め込まれてしまうのです。</p>
						<p>会員の「訪問」を狙うなら、できればコンテンツはなるべくひとつのほうがいいと思います。<br />
						多くても、せいぜい2～3コンテンツにとどめるべきでしょう。
						</p>
					</dd>
				</dl>
