					<dt>6.自社の強みを調査する</dt>
					<dd><p>件名や本文冒頭に入れる文言ですが、まずは自社が外部の環境の中でどのような状態かということを分析することで顧客への訴求内容を作成する手がかりとなります。</p>
						<p>エリア内での自社の強み女性ニーズと自社の強みとの接点<br />
						このあたりを考えているとおのずとお祝い金、待遇、話題性、希少性、新規性という箇所にたどり着くかと思われます。</p>
						<p>もちろん件名の箇所にありましたように待遇訴求、期間訴求、追い込み訴求などの手法を組み合わせることで、より訴求力の高い文章が作成できます。</p>
