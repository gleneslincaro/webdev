 <h2>第２１条（申込・募集条件、求人広告掲載基準）</h2>
 <p>１．申込条件（下記の項目に該当する場合は申込できないものとします。）<br>
  　・ネットワークビジネス、ねずみ講、マルチ商法まがい、またはその疑いがある場合。<br>
  　・許可や資格、免許が必要な業務において、無許可や無資格・無免許の場合。<br>
  　・joyspeと競合するサービスを主力事業として営んでいる業態。<br>
  　（例：求人サイト、求人ポータルサイトの運営）<br>
  　・海外法人となり日本国内に現地法人がない場合。<br>
  　・会社の実態が不明確、または確認が困難な企業、団体、組織の場合。<br>
  ２．募集条件（下記の項目に該当する場合は掲載できないものとします。）<br>
  　・業務委託、請負の求人。<br>
  　・求人内容、業務内容が不明確な求人。<br>
  　・最低賃金以下の求人。<br>
  　・初期費用がかかる、または、商品の購入を必要とする求人。<br>
  　・ねずみ講、マルチ商法まがい、またはその疑いがある求人。<br>
  　・求職者からのクレームが多発する求人情報、またはその恐れがある求人。<br>
  　・法律、条令、政令、条約に抵触する業務内容、<br>
  　また、一般的な倫理や道徳に反する恐れがある内容の求人。<br>
  　・甲が掲げる「求人情報の記述方法」の禁止事項にあたる求人。<br>
  　・事実と異なる求人。<br>
  ３．求人の記述方法<br>
  joyspeでは、作成していただいた求人情報が下記に該当する場合は<br>
  掲載内容の変更、削除、改善の指導をさせていただきます。<br>
  　・求人ページ上に直接連絡できる窓口の情報が掲載されている場合。<br>
  例）<br>
  　＊電話番号<br>
  　＊メールアドレス<br>
  　＊WEB応募の前に履歴書の送付を求める旨の指示<br>
  　＊他サイトの採用ページや採用窓口が記載されたページのURL<br>
  　・登録内容に虚偽の事実がある。<br>
  　・短縮URLやHTMLタグが記載されている場合。<br>
  　・求人広告でなく、単なる店舗の宣伝広告である場合。<br>
  　・半角カタカナ、丸付き数字などの機種依存文字を使用した場合。<br>
  　・他者（個人・法人）が著作権を有する画像を使用している場合。<br>
  　・18歳未満（高校生年齢）に対しての求職を連想させる表現を用いる場合。<br>
  　・18歳未満（高校生年齢）に対しての求職を行っている場合。<br>
  　・売春等を連想させる表現を用いて掲載している場合。<br>
  　・広告用バナー画像に「女性の乳首」「男性・女性性器」が視認できる。<br>
  　・本サービス及び類似サービスで、登録抹消や警告などを受けたことがある。<br>
  　・本サービスに申請した掲載サイトを乙が管理していない、又は出来ない。<br>
  　・管理者や管理責任の所在が不明確。<br>
  　・掲載サイトおよび乙と連絡が取れない、又はその恐れがある。<br>
  　・隠しページや騙しリンク、それに類似する事由によりユーザを惑わせる恐れがある。<br>
  　・宗教団体の勧誘、または布教活動にかかわる恐れがある場合。<br>
  　・第三者に不当で不利益を与える恐れがある場合。<br>
  　・第三者の財産権、プライバシーなどを侵害する恐れがある場合。<br>
  　・第三者を誹謗中傷する恐れがある場合。<br>
  　・残虐、または猟奇的で不快感・犯罪的行為の誘発する恐れがある場合。<br>
  　・選挙事前活動又はこれに類する恐れがある場合。<br>
  　・詐欺的なもの、もしくはこれに類する恐れがある場合。<br>
  　・本サイトの運営を妨げる恐れがある場合。<br>
  上記以外でも弊社の判断により、掲載をお断りする場合や掲載内容の変更、<br>
  削除、改善を指導させていただきます。<br>
  <br>
  年齢表記について厚生省公布の改正雇用対策法に則って「労働者の募集及び採用の際には、原則として年齢を不問」とします。よって求人ページ及びそれに基づく情報を記載するページにて、募集年齢の"上限を定める表現"はできません。<br>
  <br>
  表現可<br>
  　・18歳以上(18歳を含めた表現をする場合は"高校生不可"と表記)<br>
  　・フロアレディ20歳以上<br>
  　・○○歳代の方が多く在籍しています<br>
  　※地域条例に基づいた年齢表記をする事<br>
  　※求人年齢の上限は雇用対策法7条に基づき表記はしない事<br>
  <br>
  表現不可<br>
  　・年齢不問　・18歳～○○歳位まで　・18歳～○○歳まで　・○○歳～○○歳<br>
  <br>
  誇大表記について「トップクラス」や「地域最大」などの表現はしない。<br>
  <br>
  表現可<br>
  　・「～クラス」「～級」「～レベル」等、表現を和らげているもの<br>
  <br>
  表現不可<br>
  　・（地域などで）一番<br>
  　・（地域などで）トップ<br>
  　・（規模などが）最大<br>
  　・（待遇や給料などが）最高<br>
  <br>
  時給・日給・月給額の表記下記の金額を表記の上限とします。ただし各々『35,000円以上可』『35,000円+a』などの表現は可。グループ名の表記、グループ店・姉妹店の情報掲載及び紹介は一切禁止。<br>
  <br>
  最高額として<br>
  ・時給 5,000円　・日給 35,000円　・月給 800,000円<br>
  保証の表記時給、日給、月給の規定に準ずる。</p>
