 <h2>第２２条（本契約の範囲、変更）</h2>
 <p>１．本契約の規定とその他の本契約等の規定が異なる場合は、当該その他の本契約等の規定が優先して適用されるものとします。本契約の変更は、甲の判断により、本契約をいつでも任意の理由で変更することができるものとします。<br>
  ２．変更後の本契約は、甲が別途定める場合を除いて、本サイト上に表示した時点より効力を生じるものとします。<br>
  ３．乙が、本契約の変更の効力が生じた後に本サービスをご利用になる場合には、変更後の本契約の全ての記載内容に同意したものとみなされます。</p>
